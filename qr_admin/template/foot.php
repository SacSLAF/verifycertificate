<script>

    function confirmDelete(id) {
        Swal.fire({
            title: 'Are you sure?',
            text: "You won't be able to revert this!",
            icon: 'warning',
            showCancelButton: true,
            confirmButtonColor: '#3085d6',
            cancelButtonColor: '#d33',
            confirmButtonText: 'Yes, delete it!',
            cancelButtonText: 'Cancel'
        }).then((result) => {
            if (result.isConfirmed) {
                // Redirect to the delete action URL
                window.location.href = 'control/certificate_process.php?action=delete&id=' + id;
            }
        });
    }

</script>
<script>
  $(document).ready(function() {
    // Check if a specific message needs to be displayed
    var showMessage = "<?php echo $message; ?>";

    if (showMessage !== '') {
      var placementFrom = "top";
      var placementAlign = "right";
      var state = "primary";
      var style = "With Icon";
      var content = {};

      content.message = showMessage;
      content.title = "Notification";
      if (style == "withicon") {
        content.icon = "fa fa-bell";
      } else {
        content.icon = "none";
      }
      content.url = "index.html";
      content.target = "_blank";

      $.notify(content, {
        type: state,
        placement: {
          from: placementFrom,
          align: placementAlign,
        },
        time: 1000,
        delay: 0,
      });
    }
  });
</script>